        private bool isValidEmailAddress(string EmaillAdd)
        {
            string email = EmaillAdd;
            string pattern = @"^([\w\.\-]+)@([\w\-]+)((\.(\w){2,3})+)$";
            Regex regex = new Regex(pattern, RegexOptions.IgnorePatternWhitespace);
            Match match = regex.Match(email);
            if (match.Success)
                return true;
            else
                return false;
        }

